type Language = 'en' | 'ar';
type Dictionary = typeof dictionaries.en;

const LanguageContext = createContext<{ lang: Language; t: Dictionary }>({
  lang: 'en',
  t: dictionaries.en,
});

export const LanguageProvider = ({ children, lang }: { children: React.ReactNode; lang: Language }) => {
  return <LanguageContext.Provider value={{ lang, t: dictionaries[lang] }}>{children}</LanguageContext.Provider>;
};

export const useLanguage = () => useContext(LanguageContext);